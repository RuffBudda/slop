/**
 * Get SVG icon by name
 * @param {string} name - Icon name
 * @param {string} className - Optional CSS class name
 * @param {object} options - Optional settings (size, color)
 * @returns {string} SVG HTML string
 */
function getIcon(name, className = '', options = {}) {
  const icon = ICONS[name];
  if (!icon) {
    console.warn(`Icon "${name}" not found`);
    return '';
  }
  
  const size = options.size || '1em';
  const color = options.color || 'currentColor';
  
  // Extract SVG content and add attributes
  const svgMatch = icon.match(/<svg[^>]*>/);
  if (svgMatch) {
    let svgTag = svgMatch[0];
    // Add or update attributes
    svgTag = svgTag.replace(/width="[^"]*"/, '');
    svgTag = svgTag.replace(/height="[^"]*"/, '');
    svgTag = svgTag.replace(/<svg/, `<svg width="${size}" height="${size}" fill="${color}"`);
    if (className) {
      svgTag = svgTag.replace(/<svg/, `<svg class="${className}"`);
    }
    return icon.replace(/<svg[^>]*>/, svgTag);
  }
  
  return icon;
}

/**
 * Render icon as HTML element
 * @param {string} name - Icon name
 * @param {object} options - Optional settings
 * @returns {HTMLElement} Icon element
 */
function renderIcon(name, options = {}) {
  const div = document.createElement('div');
  div.innerHTML = getIcon(name, options.className || '', options);
  return div.firstElementChild;
}

